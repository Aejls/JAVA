//Write the following expression in a java program:
//(v^2-u^2)/2as
public class WriteExpression {
    public static void main(String[] args) {
        float v=40;
        float u=4;
        float a=6;
        float s=8;
        float formula=(v*v - u*u) /(2*a*s);
        System.out.println(formula);
    }
}
